/*Sum of Elements: Write a function to calculate 
the sum of all elements in an array.
*/


  public class SumOfElement1{
  
  public static void main (String []args){
  
  int sum=0;
    int arr[]={4,5,6,44};
	
	for(int i=0;i<=arr.length-1 ;i++){
	
	sum+=arr[i];
	 
	
	}
	System.out.println(sum);
  
  
  }
  }