/*Calculate Average: Compute the average of numbers in an array.*/

public class Avg12{

 public static void main(String []args){
 
  int arr[]={4,5,741,1,4};
  
  
  int avg=0;
  
  
 
 for(int i=1;i<arr.length ;i++){
 
   avg+=arr[i];
 
 
 
 }
 System.out.println(avg/arr.length);
  }
  
  
  
 }

  
  
  
  